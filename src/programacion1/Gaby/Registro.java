    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        String idString = txtId.getText();
        String nombre = txtNombre.getText();
        String nickname = txtNickname.getText();
        String contraseña = txtContraseña.getText();
        String edadString = txtEdad.getText();
        String telefono = txtTelefono.getText();
        String direccion = txtDireccion.getText();

        // Verificar campos vacíos
        if (idString.isEmpty() || nombre.isEmpty() || nickname.isEmpty() || contraseña.isEmpty() ||
            edadString.isEmpty() || telefono.isEmpty() || direccion.isEmpty()) {
            JOptionPane.showMessageDialog(Registro.this, "Por favor, complete todos los campos.");
        } else {
            try {
                // Convertir los valores de id y edad a enteros
                int id = Integer.parseInt(idString);
                int edad = Integer.parseInt(edadString);

                // Crear instancia de Usuario y asignar valores
                Usuario usuario = new Usuario(id, nombre, nickname, contraseña, edad, telefono, direccion);

                // Redirigir al usuario a la ventana de inicio de sesión
                Login login = new Login(usuario);
                login.setVisible(true);
                dispose(); // Cerrar ventana actual de registro
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(Registro.this, "Por favor, ingrese valores numéricos válidos para el ID y la Edad.");
            }
    }//GEN-LAST:event_btnRegistrarActionPerformed
        }

